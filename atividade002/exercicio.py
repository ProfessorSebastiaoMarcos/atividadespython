#Estudo de Variáveis: Operadores Aritméticos

#Declarações
a = 10
b = 2
#Operações
#Soma
soma = a + b
#Subtração
subtracao = a - b
#Produto
produto = a * b
#Divisão
divisao = a / b
#Potência
potencia = a ** b
#Divisão Inteira
divisaoInteira = a // b
#Resto da Divisão
restoDivisao = a % b

#Saída Formatada
print('-'*40)
print('ESTUDO DE OPERADORES ARITMÉTICOS')
print('='*40)
print(f'O valor de {a} + {b} = {soma}')
print(f'O valor de {a} - {b} = {subtracao}')
print(f'O valor de {a} * {b} = {produto}')
print(f'O valor de {a} / {b} = {divisao}')
print(f'O valor de {a} ** {b} = {potencia}')
print(f'O valor de {a} // {b} = {divisaoInteira}')
print(f'O valor de {a} % {b} = {restoDivisao}')
print('='*40)
print('Fim do Programa!')
print('-'*40)